package Methods;

public class MethodsDemo {
	
	//No Parameter No Return 
	
	void m1()
	{
		System.out.println("Hello ...Java");
	}
	
	//No Parameter but with return 
	
	String m2()
	{
		return ("Hello How are you..");
	}
	
	void m3(String name)
	{
		System.out.println("How are you : "+ name);
	}
	
	String m4(String name)
	{
		return ("Hello :"+ name);
	}

}
